package com.company;

import java.util.Objects;

public class Coordinate {


    private final int y;
    private final int x;

    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }

}
